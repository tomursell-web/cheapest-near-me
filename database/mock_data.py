def search_products(query: str) -> list:
    """Search products by name, brand, category or tags using fuzzy matching."""
    query_lower = query.lower().strip()
    results = []
    for product in PRODUCTS:
        score = 0
        if query_lower in product["name"].lower():
            score += 10
        if query_lower in product["brand"].lower():
            score += 8
        if query_lower in product["category"].lower():
            score += 6
        for tag in product["tags"]:
            if query_lower in tag.lower():
                score += 4
            elif any(word in tag.lower() for word in query_lower.split()):
                score += 2
        if score > 0:
            results.append({**product, "score": score})
    results.sort(key=lambda x: x["score"], reverse=True)
    return results


